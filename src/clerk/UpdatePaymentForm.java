    private void setValues(){
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/learnersms", "root","");
            
            String SQL="SELECT * FROM paymentinfo WHERE vehicleCategory=?";
            PreparedStatement pstm=conn.prepareStatement(SQL);
            pstm.setObject(1,UpdateStudent.category);
            ResultSet rst=pstm.executeQuery();
            if (rst.next()) {
                tot=rst.getString(2);
                txttot.setText(tot);
                txtreg.setText(rst.getString(3));
                txtotpaid.setText(getTotalPaid()+"");
            }else{
                JOptionPane.showMessageDialog(this, "Error in the category!");
            }            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(UpdatePaymentForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(UpdatePaymentForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private double getTotalPaid(){
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/learnersms", "root","");
            String SQL2="SELECT paidAmount,paymentType FROM studentpayment WHERE studentID=?";
            PreparedStatement pstm=conn.prepareStatement(SQL2);
            pstm.setObject(1,lblid.getText());
            ResultSet rst=pstm.executeQuery();
            double i=0;
                while (rst.next()){
                    if (rst.getString(2).equals("installment")) {
                    rbinstallment.setSelected(true);
                }else if (rst.getString(2).equals("full")) {
                    rbfull.setSelected(true);
                }
                    i=i+Double.parseDouble(rst.getString(1));
                }
                
            return i;
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(UpdatePaymentForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(UpdatePaymentForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        return 0.00;
    }
    
    private void btnpaymentsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnpaymentsActionPerformed
        if(!txtpayment.getText().equals(null)){
         try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/learnersms", "root","");
            String sql="INSERT INTO studentpayment(paymentType,totalAmount,studentID,paidAmount,AmountToBePaid,Date) VALUES (?,?,?,?,?,?)";
            PreparedStatement pstm=conn.prepareStatement(sql);
            pstm.setObject(1,type);
            pstm.setObject(2,tot);
            pstm.setObject(3,lblid.getText());
            pstm.setObject(4,txtpayment.getText());
            pstm.setObject(5,txttobepaid.getText());
            pstm.setObject(6,LocalDate.now()+"");
    
            if (pstm.executeUpdate()>0) {
                    JOptionPane.showMessageDialog(this, "Payment Done!!!");
                    this.hide();
            }else{
                JOptionPane.showMessageDialog(this, "Payment was unsuccessfull!!!");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(RegisterStudent.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(RegisterStudent.class.getName()).log(Level.SEVERE, null, ex);
        }
        }else{
            JOptionPane.showMessageDialog(this, "Please enter you payment amount!!!");
        }
    }//GEN-LAST:event_btnpaymentsActionPerformed

