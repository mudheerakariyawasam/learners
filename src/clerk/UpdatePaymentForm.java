    private void txtpaymentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtpaymentActionPerformed
        if(rbfull.isSelected()){
            type="full";
            txttobepaid.setText("");
            JOptionPane.showMessageDialog(this, "You have completed your payment!!!");
            this.hide();
        }else if(rbinstallment.isSelected()){
            type="installment";
            double total=Double.parseDouble(txttot.getText());
            double regFee=Double.parseDouble(txtreg.getText());
            double paid=Double.parseDouble(txtotpaid.getText());
            double pay=Double.parseDouble(txtpayment.getText());

            double tobeppaid=total-(regFee+paid+pay);
            txttobepaid.setText(tobeppaid+"");
        }else{
            JOptionPane.showMessageDialog(this, "Please select your payment type!!!");
        }
    }//GEN-LAST:event_txtpaymentActionPerformed

