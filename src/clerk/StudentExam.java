    private void search(){
    try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/learnersms", "root","");
            
            String SQL="SELECT fullName,vehicleCategory FROM student WHERE studentID=?";
            PreparedStatement pstm=conn.prepareStatement(SQL);
            pstm.setObject(1,txtid.getText());
            ResultSet rst=pstm.executeQuery();
            if (rst.next()) {
                txtname.setText(rst.getString(1));
                txtcategory.setText(rst.getString(2));
                
                try{
                    //////////////////set written exam details///////////////////////////////
                    String sql2="SELECT * FROM studentwrittenexam WHERE studentID=?";
                    PreparedStatement pstm2=conn.prepareStatement(sql2);
                    pstm2.setObject(1,txtid.getText());
                    ResultSet resultSet=pstm2.executeQuery();
                    if (resultSet.next()) {
                        String getExamDate="SELECT * FROM writtenexam WHERE writtenExamID=?";
                        PreparedStatement getED=conn.prepareStatement(getExamDate);
                        getED.setObject(1,resultSet.getString(2));
                        ResultSet rstED=getED.executeQuery();
                        //txtwritten.setText(rstED.getString(2));
                        if (resultSet.getString(3).equals("Pass")){
                            rbwp.setSelected(true);
                        }else if (resultSet.getString(3).equals("Fail")){
                            rbwf.setSelected(true);
                        }

                    }else{
                    }
                }catch(Exception ex){
                      ex.printStackTrace();
                }
                    /////////////////////////////////////////////////////////////////////////////////

                    /////////////////////set trial details///////////////////////////////////////////
                try{
                    String sql2="SELECT * FROM studenttrial WHERE studentID=?";
                    PreparedStatement pstm2=conn.prepareStatement(sql2);
                    pstm2.setObject(1,txtid.getText());
                    ResultSet resultSet=pstm2.executeQuery();
                    if (resultSet.next()) {
                        String getTrialDate="SELECT date FROM trial WHERE trialID=? && category=?";
                        PreparedStatement getTD=conn.prepareStatement(getTrialDate);
                        getTD.setObject(1,resultSet.getString(2));
                        getTD.setObject(2,txtcategory.getText());
                        ResultSet rstTD=getTD.executeQuery();
                        //txttrial.setText(rstTD.getString(1));
                        if (resultSet.getString(3).equals("Pass")){
                            rbtp.setSelected(true);
                        }else if (resultSet.getString(3).equals("Fail")){
                            rbtf.setSelected(true);
                        }

                    }else{
                    }
                    ////////////////////////////////////////////////////////////////////////////
                }catch (Exception e){
                  e.printStackTrace();
                }
            }else{
            JOptionPane.showMessageDialog(this,"Student doesn't Exist!");
            }                            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(UpdateStudent.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(UpdateStudent.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
