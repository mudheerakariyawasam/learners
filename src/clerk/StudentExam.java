    private void btnsavewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsavewActionPerformed
        if (rbwp.isSelected()) {
            wgrade="Pass";
        }else if (rbwf.isSelected()) {
            wgrade="Fail";
        }else{
            JOptionPane.showMessageDialog(this,"Please select the Grade to Proceed!!!");
        }
        
         try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/learnersms", "root","");
            
            String getWrittenID="SELECT writtenExamID FROM writtenexam WHERE date=?";
            PreparedStatement pstm1=conn.prepareStatement(getWrittenID);
            pstm1.setObject(1,txtwritten.getText());
            ResultSet rst=pstm1.executeQuery();
            if (rst.next()) {
                String SQL="INSERT INTO studentwrittenexam VALUES (?,?,?)";
                PreparedStatement pstm=conn.prepareStatement(SQL);
                pstm.setObject(1,txtid.getText());
                pstm.setObject(2, rst.getString(1));
                pstm.setObject(3,wgrade);
                if (pstm.executeUpdate()>0) {
                    JOptionPane.showMessageDialog(this, "Student Exam Details has been added Successfully!!!");
                    clearForm();
                }
             }else{
                JOptionPane.showMessageDialog(this, "Please Check the Exam Date!!!");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(StudentExam.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(StudentExam.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnsavewActionPerformed

