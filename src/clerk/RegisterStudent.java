    private void btnregisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregisterActionPerformed
        String gender="";
        if (rbmale.isSelected()) {
            gender="Male";
        }else if (rbfemale.isSelected()) {
            gender="Female";
        }else{
            JOptionPane.showMessageDialog(this,"Please select your Gender to Proceed!!!");
        }
        
        if (rbA.isSelected()) {
            category="Type A";
        }else if (rbB.isSelected()) {
            category="Type B";
        }else if (rbC.isSelected()) {
            category="Type C";
        }else{
            JOptionPane.showMessageDialog(this,"Please select the Vehicle Category to Proceed!!!");
        }
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/learnersms", "root","");
            
            String SQL="INSERT INTO student VALUES(?,?,?,?,?,?,?,?,?,?,?)";
            String sql2="INSERT INTO (paymentType,totalAmount,studentID,paidAmount,AmountToBePaid,Date) VALUES (?,?,?,?,?,?)";
            PreparedStatement pstm=conn.prepareStatement(SQL);
            PreparedStatement pstm2=conn.prepareStatement(sql2);
            pstm.setObject(1,lblid.getText());
            pstm.setObject(2,txtname.getText());
            pstm.setObject(3,txtaddress.getText());
            pstm.setObject(4,txtnic.getText());
            pstm.setObject(5,Integer.parseInt(txtconno.getText()));
            pstm.setObject(6,txtdob.getText());
            pstm.setObject(7,gender);
            pstm.setObject(8,cmbbldgrp.getSelectedItem());
            pstm.setObject(9,txtheight.getText());
            pstm.setObject(10,txtweight.getText());
            pstm.setObject(11,category);
            
            pstm2.setObject(1,PaymentForm.type);
            pstm2.setObject(2,PaymentForm.tot);
            pstm2.setObject(3,lblid.getText());
            pstm2.setObject(4,PaymentForm.payment);
            pstm2.setObject(5,PaymentForm.tobepaid);
            pstm2.setObject(6,LocalDate.now()+"");
    
            if (pstm.executeUpdate()>0) {
                if (pstm2.executeUpdate()>0) {
                    JOptionPane.showMessageDialog(this, "Student has been Reegistered Successfully!!!");
                    clearForm();
                }else{
                    JOptionPane.showMessageDialog(this, "Registration was unsuccessfull!!!");
                    PreparedStatement stm=conn.prepareStatement("DELETE FROM student WHERE studentID=?");
                    stm.setObject(1,lblid.getText());
                    stm.executeUpdate();
                    clearForm();
            }}else{
            JOptionPane.showMessageDialog(this, "Registration was unsuccessfull!!!");
                clearForm();
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(RegisterStudent.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(RegisterStudent.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnregisterActionPerformed

