    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/learnersms","root","");
            String sql="INSERT INTO trial VALUES (?,?,?,?)";
            PreparedStatement pstm=conn.prepareStatement(sql);
            pstm.setObject(1, lblid.getText());
            pstm.setObject(2, txtdate.getText());
            pstm.setObject(3, cmbtime.getSelectedItem()+"");
            pstm.setObject(4, txtno.getText());
            if (pstm.executeUpdate()>0) {
                JOptionPane.showMessageDialog(this, "Trial Schedule Added Successfully!!!");
                loadAllSchedules();
                lblid.setText(getTrialID());
                txtdate.setText("");
                txtno.setText("");
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TrialExamSchedule.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(TrialExamSchedule.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnaddActionPerformed

