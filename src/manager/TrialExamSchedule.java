    private String getTrialID() {
        try{
        Class.forName("com.mysql.jdbc.Driver");
        Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/learnersms","root","");
        String sql="SELECT trialID FROM trial ORDER BY trialID DESC LIMIT 1";
        PreparedStatement pstm=conn.prepareStatement(sql);
        ResultSet set = pstm.executeQuery();
        String id="T001";
        if (set.next()){
            String temp=set.getString(1);
            String[] cs = temp.split("T");
            int selectedId=Integer.parseInt(cs[1]);
            if (selectedId<10){
                id="T00"+(selectedId+1);
            }else{
                id="T0"+(selectedId+1);
            }
        }
        return id;
        }catch (ClassNotFoundException ex) {
            Logger.getLogger(AddEmployee.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddEmployee.class.getName()).log(Level.SEVERE, null, ex);
        }
        return null;
    }
    
